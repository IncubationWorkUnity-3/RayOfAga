//---AGANOEIのScene説明---//
/*
 * AGANOEIプロジェクトはPlugins、resources、Scene、scriptの4つのフォルダに分かれています。
 * 今回はその中のひとつ、Sceneフォルダについて説明していきます。
 * 
 * ---Sceneフォルダとは---
 * SceneフォルダとはAGANOEIのsceneを管理するフォルダの事です。
 * この中には、本番で使うSCENEフォルダ、練習用で使うtest.scene、title.scene、title.scriptが存在します。
 * 練習用のsceneは自由に使って貰って構いません。
 * ただ、本番で使うSCENEフォルダは丁寧に扱って下さい。(じゃないと爆発します)
 * 
 * ---SCENEフォルダの中身---
 * SCENEフォルダの中身には、Title.scene、Ending.scene、GMain.scene、GOver.sceneが存在します。
 * それぞれの役割は、
 * Title.scene → タイトル画面
 * Ending.scene → エンディング画面
 * GMain.scene → ゲーム画面(メイン)
 * GOver.scene → ゲームオーバー画面
 * となっております。
 * 
 */
